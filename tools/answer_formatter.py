import os
from dotenv import load_dotenv
from groq import Groq

load_dotenv()

client = Groq(
    api_key=os.getenv("GROQ_API_KEY")
)

def format_answer(question, raw_result):

    prompt = f"""
Question:
{question}

Raw Result:
{raw_result}

Convert the raw result into a clean, concise answer.
Do not show JSON or Python dictionaries.
"""

    response = client.chat.completions.create(
        model="llama-3.3-70b-versatile",
        messages=[
            {
                "role": "user",
                "content": prompt
            }
        ]
    )

    return response.choices[0].message.content